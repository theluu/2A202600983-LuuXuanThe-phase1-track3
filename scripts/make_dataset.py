def build() -> list[dict]:
    out = []
    for i, (difficulty, q, gold, c1, c2) in enumerate(ROWS, start=1):
        out.append({
            "qid": f"bm{i:03d}",
            "difficulty": difficulty,
            "question": q,
            "gold_answer": gold,
            "context": [
                {"title": c1[0], "text": c1[1]},
                {"title": c2[0], "text": c2[1]},
            ],
        })
    return out


if __name__ == "__main__":
    data = build()
    path = Path("data/benchmark_set.json")
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False), encoding="utf-8")
    print(f"Wrote {len(data)} examples to {path}")
